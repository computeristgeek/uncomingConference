<h1>About Alexandria</h1>
<p class="white shaded enlargeFonts" >Alexandria is the second largest city and a major economic centre in Egypt, extending about 32 km (20 mi) along the coast of the Mediterranean Sea in the north central part of the country. It is also the largest city lying directly on the Mediterranean coast. Its low elevation on the Nile delta makes it highly vulnerable to rising sea levels. Alexandria is Egypt's largest seaport, serving approximately 80% of Egypt's imports and exports. It is an important industrial center because of its natural gas and oil pipelines from Suez. Alexandria is also an important tourist destination.</p>
